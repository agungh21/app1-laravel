     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('dashboard.authors.index', [
            'authors' => User::filter(request(['search']))->paginate(10)->withQueryString()
        ]);
    }

